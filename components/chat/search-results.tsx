
type WebSearchResponse = {
    query: string // search query
    answer: string // generated by Tavilly AI
    results: {
        count: number
        data: {
            reference: number
            title: string
            url: string
            content: string
            score: number // closeness score?
        }[]
    }
}

export default function SearchResults({
    data,
}: {
    data: WebSearchResponse
}) {

    if (!(data && data.results)) return null;

    return (
        <div className="ml-3">
            <p className='text-sm'>Search results for <span className='font-medium text-black'>{`"${data.query}"`}</span></p>
            {data.results.data.map((source, index) => (
            <div key={`source-${source.title}`}>
                <span className="text-sm">{index + 1}.</span> <a href={source.url} target="_blank" className="underline text-sky-600 text-sm">{source.title}</a>
            </div>
            ))}
        </div>
    )
}